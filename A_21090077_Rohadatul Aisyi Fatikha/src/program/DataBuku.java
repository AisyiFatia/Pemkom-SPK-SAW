    private void txt_tahunKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_tahunKeyTyped
        char c = evt.getKeyChar();
        if(Character.isLetter(c)&&!evt.isAltDown()){
            evt.consume();
        }
    }//GEN-LAST:event_txt_tahunKeyTyped

    private void cekKosong() throws exceptionData
    {
       if(txt_judul.getText().isEmpty()==true || txt_tahun.getText().isEmpty()==true
               || txt_sedia.getText().isEmpty() == true || txt_resensi.getText().isEmpty() == true
                || txt_minat.getText().isEmpty() == true)
            throw new exceptionData();
    }
    
    public int simpan(){        
        String sql="insert into tbl_kriteria "+
	"values"+
	"('"+txt_judul.getText()+"', "+
	"'"+txt_tahun.getText()+"', "+
	"'"+txt_sedia.getText()+"', "+
	"'"+txt_resensi.getText()+"', "+
        "'"+txt_minat.getText()+"'"+
	")";    
        
        clear();
        
        conn.simpanData(sql);
        
       return 0;
    }
    
    private void clear(){
        txt_judul.setText("");
        txt_tahun.setText((""));
        txt_sedia.setText("");
        txt_resensi.setText("");
        txt_minat.setText("");
    }
    
    public void tabelBuku(){
       tabelDataBuku = new DefaultTableModel();
       tabelDataBuku.addColumn("Judul Buku");
       tabelDataBuku.addColumn("Tahun Terbit ");
       tabelDataBuku.addColumn("Ketersediaan Buku");
       tabelDataBuku.addColumn("Resensi Buku");
       tabelDataBuku.addColumn("Minat Buku");
       tbl_data_buku.setModel(tabelDataBuku);
        try {
         ResultSet res = conn.ambilData("SELECT * FROM tbl_kriteria ");
        while (res.next()){
                    tabelDataBuku.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5)});
                } 
      
        tbl_data_buku.setModel(tabelDataBuku);
          } catch (SQLException ex) {
           JOptionPane.showMessageDialog(this, ex);
        }   
     }
    
    public void inputAngka(java.awt.event.KeyEvent evt){
        char cDigit = evt.getKeyChar();
        if(!Character.isDigit(cDigit)){
            evt.consume();
        }
    }
    
